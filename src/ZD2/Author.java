package ZD2;

public class Author {
    private int id;
    private String firstName;
    private String lastName;
    private String pseudonym;

    public Author(int id, String imię, String nazwisko, String pseudonim){
        this.id= id;
        this.firstName = imię;
        this.lastName= nazwisko;
        this.pseudonym= pseudonim;
    }
}

